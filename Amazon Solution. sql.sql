    LEFT JOIN order_item i ON o.order_id = i.order_id
    GROUP BY c.customer_id, customer_name
),
y AS 
(
    SELECT 
        customer_id,
        ROUND((total_revenue / NULLIF(total_orders, 0)), 2) AS Avg_order_value 
    FROM x
)
